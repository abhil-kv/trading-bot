-- Function: Update strategy performance
CREATE OR REPLACE FUNCTION update_strategy_performance()
RETURNS TRIGGER AS $$
BEGIN
    -- This function can be called after a signal is closed
    -- to update the strategy_performance table
    -- Implementation depends on specific requirements
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Function: Calculate holding period
CREATE OR REPLACE FUNCTION calculate_holding_period()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.exit_time IS NOT NULL AND NEW.entry_time IS NOT NULL THEN
        NEW.holding_minutes := EXTRACT(EPOCH FROM (NEW.exit_time - NEW.entry_time)) / 60;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger: Update holding period for ORB signals
CREATE TRIGGER trg_orb_holding_period
BEFORE UPDATE ON orb_signals
FOR EACH ROW
WHEN (NEW.exit_time IS NOT NULL)
EXECUTE FUNCTION calculate_holding_period();

-- Function: Update timestamp
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Triggers: Update updated_at on all signal tables
CREATE TRIGGER trg_mean_reversion_updated_at
BEFORE UPDATE ON mean_reversion_signals
FOR EACH ROW
EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER trg_swing_updated_at
BEFORE UPDATE ON swing_signals
FOR EACH ROW
EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER trg_orb_updated_at
BEFORE UPDATE ON orb_signals
FOR EACH ROW
EXECUTE FUNCTION update_updated_at_column();

